//! Pre-computed energy-spread weighting tables for Q65's fast-fading
//! intrinsic-probability metric.
//!
//! Direct port of `lib/qra/q65/fadengauss.c` and `fadenlorentz.c` from
//! the WSJT-X distribution. Each `*_TAPS[i]` is the first half (centre
//! tap last) of an odd-length symmetric weighting window, calibrated
//! for one of 64 fractional-energy spread bandwidths in increments of
//! `1.09×`. The corresponding length in **one-sided taps** (i.e.
//! `(L+1)/2` where `L` is the full odd window length) is in
//! `*_LEN[i]`. The Q65 fast-fading metric picks the index from the
//! observed `B90·Ts` parameter.
//!
//! See `crate::fec::qra::fast_fading::intrinsics_fast_fading` for the
//! consuming code.

